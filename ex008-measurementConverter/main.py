m = float(input('Insert a distance in meter: '))

km = m/1000
hm = m/100
dam = m/10
dm = m*10
cm = m*100
mm = m*1000

print(f'The distance in km: {km:.2f}')
print(f'The distance in hm: {hm:.2f}')
print(f'The distance in dam: {dam:.2f}')
print(f'The distance in dm: {dm:.2f}')
print(f'The distance in cm: {cm:.2f}')
print(f'The distance in mm: {mm:.2f}')
